import turtle

window = turtle.Screen()

tom = turtle.Turtle()
tom.speed(10)
tom.pensize(20)

tom.penup()
tom.goto(-200, 0)
tom.pendown()

tom.fillcolor("yellow")
tom.begin_fill()

tom.color("yellow")
tom.forward(450)
tom.right(90)
tom.forward(100)
tom.right(90)
tom.forward(450)
tom.right(90)
tom.forward(100)
tom.end_fill()

tom.penup()
tom.goto(-50, 0)
tom.pendown()

tom.right(45)
tom.forward(100)
tom.right(45)
tom.forward(100)
tom.right(45)
tom.forward(100)

tom.penup()
tom.goto(100, 70)
tom.pendown()
tom.right(45)
tom.forward(100)

tom.penup()
tom.goto(110, -100)
tom.pendown()

tom.fillcolor("black")
tom.begin_fill()
tom.color("black")
tom.circle(35)
tom.end_fill()

tom.penup()
tom.goto(-90, -100)
tom.pendown()

tom.fillcolor("black")
tom.begin_fill()
tom.color("black")
tom.circle(35)
tom.end_fill()

tom.penup()
tom.pensize(1)
tom.goto(250, 0)
tom.right(270)
tom.color("red")
tom.fillcolor("red")
tom.pendown()
tom.begin_fill()
tom.forward(10)
tom.right(90)
tom.forward(45)
tom.right(90)
tom.forward(10)
tom.end_fill()

tom.penup()
tom.pensize(1)
tom.goto(-210, 0)
tom.right(270)
tom.color(" gray")
tom.fillcolor(" gray")
tom.pendown()
tom.begin_fill()
tom.left(90)
tom.forward(10)
tom.right(90)
tom.forward(45)
tom.right(90)
tom.forward(10)
tom.end_fill()
tom.penup()
tom.goto(250, 250)

window.exitonclick()
